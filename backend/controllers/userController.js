// API to cancel appointment

const cancelAppointment = async (req, res) => {
  try {
    const userId = req.userId;
    const { appointmentId } = req.body;

    const appointmentData = await appointmentModel.findById(appointmentId);

    // verify appointment user
    if (appointmentData.userId.toString() !== userId.toString()) {
      return res.json({ success: false, message: "unauthorized action" });
    }

    await appointmentModel.findByIdAndUpdate(appointmentId, {
      cancelled: true,
    });

    // releasing doctor slot

    const { docId, slotDate, slotTime } = appointmentData;

    const doctorData = await doctorModel.findById(docId);

    let slots_booked = doctorData.slots_booked;
    slots_booked[slotDate] = slots_booked[slotDate].filter(
      (e) => e !== slotTime
    );

    await doctorModel.findByIdAndUpdate(docId, { slots_booked });
    res.json({ success: true, message: "Appointment Cancelled" });
  } catch (error) {
    console.log(error);
    res.json({ success: false, message: error.message });
  }
};

// payment gateway for appointment using Stripe 

const paymentStripe = async (req,res) => {
       
  try {
    const { appointmentId } = req.body;
    const { origin } = req.headers;

    if (!origin) {
      return res.status(400).json({ success: false, message: "Missing origin header" });
    }

    const appointment = await appointmentModel.findById(appointmentId);

    if (!appointment) {
      return res.status(404).json({ success: false, message: "Appointment not found" });
    }

    if (appointment.payment) {
      return res.json({ success: false, message: "Already paid" });
    }

    const line_items = [
      {
        price_data: {
          currency,
          product_data: {
            name: `Appointment with Dr. ${appointment.docData?.name || appointment.docId}`,
          },
          unit_amount: appointment.amount * 100,
        },
        quantity: 1,
      },
    ];

    // Create Stripe session
    const session = await stripe.checkout.sessions.create({
      success_url: `${origin}/verify?success=true&appointmentId=${appointmentId}`,
      cancel_url: `${origin}/verify?success=false&appointmentId=${appointmentId}`,
      line_items,
      mode: "payment",
    });

    if (!session?.url) {
      console.error("Stripe session created but missing URL", session);
      return res.status(500).json({ success: false, message: "Failed to get payment URL" });
    }

    res.json({ success: true, sessionUrl: session.url });

  } catch (error) {
    console.error("paymentStripe error:", error);
    res.status(500).json({ success: false, message: error.message });
  }
  
}

